# SLAM dataset
# This dataset is created for SLAM testing and includes two cameras (a regular frame-based camera and an event-based Dynamical Visual Sensor (DVS) camera) simultaneously capturing multiple indoor and outdoor scenarios. The indoor dataset features different floors of a campus building, while the outdoor dataset was recorded around the campus.


